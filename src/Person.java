public class Person {
 private String name;
 private String Email;

 public Person()
 {

 }
 public Person(String n,String e)
 {
    name=n;
    Email=e;
 }
 void setName(String n)
 {
    name=n;
 }
 void setEmail(String E)
 {
    Email=E;
 }
 String getName()
 {
    return name;
 }
 String getEmail()
 {
    return Email;
 }

}
